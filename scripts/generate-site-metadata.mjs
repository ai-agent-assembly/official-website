#!/usr/bin/env node
// AAASM-4355 / Epic AAASM-4352
//
// Site metadata generator. Reads the canonical URLs (marketing/docs/app/api)
// and the served install-script URL from `metadata/site-metadata.yaml` and
// rewrites two artifacts in place:
//
//   1. `src/generated/site-urls.ts` — a TypeScript module of `as const`
//      string constants imported by `docusaurus.config.ts`,
//      `src/pages/product.tsx`, and `src/components/MegaMenu/menus.ts`.
//      Regenerated in full every run; drift is caught by `git diff --exit-code`.
//
//   2. `static/install.sh` — the served installer keeps its executable body
//      byte-identical to the canonical upstream (enforced by the existing
//      `installer-drift.yml` workflow), so we do NOT rewrite the body. We
//      rewrite ONLY a bounded sentinel block in the header, whose content is
//      the served install-script URL (used in the provenance comment). The
//      sentinel pattern mirrors AAASM-4337.
//
// Zero network access. Reads local repo state only.
//
// Run from the repo root:  pnpm run generate:site-metadata
// CI check:                pnpm run generate:site-metadata && git diff --exit-code

import {readFileSync, writeFileSync, mkdirSync} from 'node:fs';
import {fileURLToPath} from 'node:url';
import {dirname, resolve} from 'node:path';

// ---------------------------------------------------------------------------
// Metadata loading
// ---------------------------------------------------------------------------

// The SoT file has a fixed, flat two-level shape:
//
//   urls:
//     marketing: "..."
//     docs: "..."
//     app: "..."
//     api: "..."
//   install:
//     script_url: "..."
//
// A dedicated parser keeps the generator dependency-free (no js-yaml direct
// dep) and rejects any unexpected shape loudly so drift-catch stays honest.
export function parseSiteMetadata(text) {
  const out = {urls: {}, install: {}};
  let section = null;
  const lines = text.split(/\r?\n/);
  for (let i = 0; i < lines.length; i++) {
    const raw = lines[i];
    const line = raw.replace(/\s+$/, '');
    if (line === '' || line.trimStart().startsWith('#')) continue;

    // Top-level section header: `<key>:` with no indent, no value.
    const sectionMatch = /^([a-z_]+):\s*$/i.exec(line);
    if (sectionMatch) {
      section = sectionMatch[1];
      if (section !== 'urls' && section !== 'install') {
        throw new Error(
          `site-metadata.yaml: unexpected top-level key "${section}" on line ${i + 1}`,
        );
      }
      continue;
    }

    // Nested key: `  <key>: "<value>"` or `  <key>: '<value>'` (indented,
    // quoted string — either quote style, since prettier normalises YAML to
    // single quotes while a hand-authored SoT may still use double quotes).
    const kvMatch = /^\s+([a-z_]+):\s*(?:"([^"]*)"|'([^']*)')\s*$/i.exec(line);
    if (kvMatch) {
      if (section === null) {
        throw new Error(
          `site-metadata.yaml: key "${kvMatch[1]}" on line ${i + 1} has no parent section`,
        );
      }
      out[section][kvMatch[1]] = kvMatch[2] ?? kvMatch[3];
      continue;
    }

    throw new Error(
      `site-metadata.yaml: unparseable line ${i + 1}: ${JSON.stringify(raw)}`,
    );
  }
  return out;
}

const REQUIRED_URLS = ['marketing', 'docs', 'app', 'api'];
const REQUIRED_INSTALL = ['script_url'];

export function loadMetadata(repoRoot) {
  const yamlPath = resolve(repoRoot, 'metadata/site-metadata.yaml');
  const raw = readFileSync(yamlPath, 'utf8');
  const parsed = parseSiteMetadata(raw);
  for (const k of REQUIRED_URLS) {
    if (typeof parsed.urls[k] !== 'string' || parsed.urls[k].length === 0) {
      throw new Error(`metadata/site-metadata.yaml: missing urls.${k}`);
    }
  }
  for (const k of REQUIRED_INSTALL) {
    if (
      typeof parsed.install[k] !== 'string' ||
      parsed.install[k].length === 0
    ) {
      throw new Error(`metadata/site-metadata.yaml: missing install.${k}`);
    }
  }
  return parsed;
}

// ---------------------------------------------------------------------------
// Emitter: src/generated/site-urls.ts
// ---------------------------------------------------------------------------

// Path (relative to repo root) of the emitted TS module. Kept in one place so
// the drift-check workflow scope stays trivially auditable.
export const SITE_URLS_TS_PATH = 'src/generated/site-urls.ts';

// Prettier config in this repo: single quotes, no bracket spacing, trailing
// commas everywhere. We emit output that already conforms so `pnpm format:check`
// stays green without a post-process step.
function tsString(value) {
  return `'${String(value).replace(/\\/g, '\\\\').replace(/'/g, "\\'")}'`;
}

export function renderSiteUrlsModule(meta) {
  return [
    '// GENERATED BY scripts/generate-site-metadata.mjs — DO NOT EDIT.',
    '// AAASM-4355 / Epic AAASM-4352.',
    '//',
    '// Source of truth: metadata/site-metadata.yaml',
    '// Regenerate with:  pnpm run generate:site-metadata',
    '',
    'export const SITE_URLS = {',
    `  marketing: ${tsString(meta.urls.marketing)},`,
    `  docs: ${tsString(meta.urls.docs)},`,
    `  app: ${tsString(meta.urls.app)},`,
    `  api: ${tsString(meta.urls.api)},`,
    '} as const;',
    '',
    'export const INSTALL_SCRIPT_URL = ' +
      `${tsString(meta.install.script_url)};`,
    '',
    '// Convenience aliases — every import site names them, so name them once here.',
    'export const MARKETING_URL = SITE_URLS.marketing;',
    'export const DOCS_URL = SITE_URLS.docs;',
    'export const APP_URL = SITE_URLS.app;',
    'export const API_URL = SITE_URLS.api;',
    '',
  ].join('\n');
}

export function writeSiteUrlsModule(repoRoot, meta) {
  const abs = resolve(repoRoot, SITE_URLS_TS_PATH);
  const body = renderSiteUrlsModule(meta);
  let previous;
  try {
    previous = readFileSync(abs, 'utf8');
  } catch {
    previous = null;
  }
  const changed = previous !== body;
  if (changed) {
    mkdirSync(dirname(abs), {recursive: true});
    writeFileSync(abs, body);
  }
  return {changed};
}

// ---------------------------------------------------------------------------
// Rewriter: static/install.sh sentinel block
// ---------------------------------------------------------------------------

// The served install.sh body is byte-identical to the canonical upstream
// installer (enforced by installer-drift.yml). We must therefore NOT rewrite
// the body; we only maintain a bounded sentinel block in the header comment,
// whose sole content is the served install-script URL. If the served URL ever
// changes, this generator + the SoT are the single place to update it.
//
// Sentinels are POSIX shell comments so `sh` treats them as no-ops:
//
//   # BEGIN GENERATED: install-script-url — DO NOT EDIT
//   # Served at: https://agent-assembly.com/install.sh
//   # END GENERATED: install-script-url
//
// The block is inserted after the shebang if not already present.
const INSTALL_SH_PATH = 'static/install.sh';
const INSTALL_SENTINEL_BEGIN =
  '# BEGIN GENERATED: install-script-url — DO NOT EDIT';
const INSTALL_SENTINEL_END = '# END GENERATED: install-script-url';
const INSTALL_SENTINEL_RE =
  /# BEGIN GENERATED: install-script-url — DO NOT EDIT[\s\S]*?# END GENERATED: install-script-url\n?/;

export function renderInstallSentinelBlock(meta) {
  return [
    INSTALL_SENTINEL_BEGIN,
    '# GENERATED BY scripts/generate-site-metadata.mjs — DO NOT EDIT.',
    '# Source of truth: metadata/site-metadata.yaml (install.script_url).',
    `# Served at: ${meta.install.script_url}`,
    INSTALL_SENTINEL_END,
    '',
  ].join('\n');
}

export function rewriteInstallScript(repoRoot, meta) {
  const abs = resolve(repoRoot, INSTALL_SH_PATH);
  const original = readFileSync(abs, 'utf8');
  const block = renderInstallSentinelBlock(meta);

  let updated;
  if (INSTALL_SENTINEL_RE.test(original)) {
    updated = original.replace(INSTALL_SENTINEL_RE, block);
  } else {
    // First run: inject the block on the line after the shebang. The very
    // first line of static/install.sh is `#!/bin/sh`; we splice the block in
    // immediately after so the executable body (which the drift check anchors
    // on `set -eu`) is unaffected.
    const nl = original.indexOf('\n');
    if (nl === -1) {
      throw new Error(
        `${INSTALL_SH_PATH}: file has no newline; refusing to inject sentinel block`,
      );
    }
    updated = original.slice(0, nl + 1) + block + original.slice(nl + 1);
  }

  const changed = updated !== original;
  if (changed) {
    writeFileSync(abs, updated);
  }
  return {changed};
}

// ---------------------------------------------------------------------------
// Entrypoint
// ---------------------------------------------------------------------------

export function runGenerator({repoRoot, logger = console} = {}) {
  const meta = loadMetadata(repoRoot);
  const tsResult = writeSiteUrlsModule(repoRoot, meta);
  const shResult = rewriteInstallScript(repoRoot, meta);
  const changed = [];
  if (tsResult.changed) changed.push(SITE_URLS_TS_PATH);
  if (shResult.changed) changed.push(INSTALL_SH_PATH);
  if (changed.length === 0) {
    logger.log('[generate-site-metadata] no files required rewriting');
  } else {
    logger.log(
      `[generate-site-metadata] rewrote ${changed.length} file(s): ${changed.join(', ')}`,
    );
  }
  return {meta, changed};
}

